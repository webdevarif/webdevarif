"use client";

import { useEffect, useState } from "react";

import { Progress } from "@kit/ui/progress";

export function EntitySeoSkeleton() {
  const [elapsedMs, setElapsedMs] = useState(0);

  useEffect(() => {
    const startedAt = Date.now();
    const id = setInterval(() => {
      setElapsedMs(Date.now() - startedAt);
    }, 150);
    return () => clearInterval(id);
  }, []);

  const elapsedSec = elapsedMs / 1000;
  const pct = 95 * (1 - Math.exp(-elapsedSec / 6));
  const status = statusFor(elapsedSec);

  return (
    <div className="rounded-lg border border-border bg-card px-4 py-3">
      <Progress
        value={pct}
        label={
          <span className="font-mono text-xs text-muted-foreground">
            {`// ${status} · ${elapsedSec.toFixed(1)}s elapsed`}
          </span>
        }
      />
    </div>
  );
}

function statusFor(seconds: number): string {
  if (seconds < 2) return "fetching page";
  if (seconds < 5) return "scanning JSON-LD entity markup";
  if (seconds < 15) return "LLM mapping entities to knowledge graph";
  if (seconds < 25) return "scoring entity authority";
  return "almost there — LLM is slow today";
}
